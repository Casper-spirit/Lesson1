name = "Это моя строка"    # строка
print(name[0])             # первый символ
print(name[-1])             # последний символ
print(name[2:6])             # с третьего по пятый символы
print(name[::-1])             # наоборот
print(len(name))               # кол-во символов в строке
name1 = "'ЭТО НОВАЯ СТРОКА'"   # новая строка
print(name1 + name)            # сложение строк

